function idiot(troco){
    var result
    switch(troco){
        case 'giro':
            result = 'bizonho'
            break
        case 'xuxu':
            result = 'eita'
            break
        case 'aaa':
            result = 'blaba'
            break
        default:
            result = "fabercastel"
    }
    return result
}
